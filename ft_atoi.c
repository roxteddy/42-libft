int		ft_atoi(const char *str)
{
	int		sum;
	int		sign;

	sum = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if ((sign = (*str == '-' ? -1 : 1)) == -1 || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		sum = sum * 10 + *str++ - '0';
	return (sum * sign);
}
